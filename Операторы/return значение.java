// Возвращение результата (вернуть результат из самого метода)
return значение;
// Где return — это оператор, который немедленно завершит работу метода. А значение — это значение, которое должен вернуть метод вызывающему методу в качестве результата. 
// Тип значения обязан совпадать с типом, указанным при объявлении метода.

// Пример 1. Метод вычисляет минимум из двух чисел:
int min(int a, int b)
{
   if (a < b)
     return a;
   else
     return b;
}

// Пример 2: Метод вычисляет максимум из двух чисел с помощью тернарного оператора:
int max(int a, int b) // Метод возвращает максимум из двух чисел.
{
   return (a > b ? a : b); // вернуть (если a > b, то a, иначе b)
}

// Пример 3. Метод дублирует переданную в него строку n раз:
String multiple(String str, int times) // Метод принимает два параметра — строку и количество ее повторений.
{
   String result = ""; // Создается пустая строка для будущего результата.
   for (int i = 0; i < times; i++) // В цикле times раз
     result = result + " "+ str; // к строке result добавляется пробел и строка str.
   return result; // В качестве результата метода возвращается переменная result.
}